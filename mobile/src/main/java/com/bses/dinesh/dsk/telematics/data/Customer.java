package com.bses.dinesh.dsk.telematics.data;

public class Customer
{
    public String orderNum;
    public String firstName;
    public String lastName;
    public String house_no;
    public String building_name;
    public String street;
    public String area;
    public String pin;
    public String gender;
    public String father_name;
    public String mother_name;
    public String mobile_no;
    public String email;
    public String entry_date_1;
    public String status;

    public Customer() {}

    public String getOrderNum() {
        return orderNum;
    }
    public void setOrderNum(String orderNum) {
        this.orderNum = orderNum;
    }

    public String getFirstName() {
        return firstName;
    }
    public void setFirstName(String firstName) {
        this.firstName = firstName;
    }

    public String getLastName() {
        return lastName;
    }
    public void setLastName(String lastName) {
        this.lastName = lastName;
    }

    public String getHouse_no() {
        return house_no;
    }
    public void setHouse_no(String house_no) {
        this.house_no = house_no;
    }

    public String getBuilding_name() {
        return building_name;
    }
    public void setBuilding_name(String building_name) {
        this.building_name = building_name;
    }

    public String getStreet() {
        return street;
    }
    public void setStreet(String street) {
        this.street = street;
    }

    public String getArea() {
        return area;
    }
    public void setArea(String area) {
        this.area = area;
    }

    public String getPin() { return pin; }
    public void setPin(String pin) {
        this.pin = pin;
    }

    public String getGender() { return gender; }
    public void setGender(String gender) {
        this.gender = gender;
    }

    public String getFather_name() { return father_name; }
    public void setFather_name(String father_name) {
        this.father_name = father_name;
    }

    public String getMother_name() { return mother_name; }
    public void setMother_name(String mother_name) {
        this.mother_name = mother_name;
    }

    public String getMobile_no() { return mobile_no; }
    public void setMobile_no(String mobile_no) {
        this.mobile_no = mobile_no;
    }

    public String getEmail() { return email; }
    public void setEmail(String email) {
        this.email = email;
    }

    public String getEntry_date_1() { return entry_date_1; }
    public void setEntry_date_1(String entry_date_1) {
        this.entry_date_1 = entry_date_1;
    }

    public String getStatus() { return status; }
    public void setStatus(String status) {
        this.status = status;
    }
}
